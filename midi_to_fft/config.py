from dataclasses import dataclass


@dataclass
class AudioConfig:
    sample_rate: int = 22050

    # ── Параметры сегментации трека ──────────────────────────
    segment_size_sec: float = 5.0   # длина одного сегмента (секунд)

    # ── Параметры STFT / Mel ─────────────────────────────────
    # n_fft=2048 соответствует ~93 мс при sr=22050 — стандарт для музыки.
    # Прежде был 4096: это давало 7-кратное zero-padding при окне 551 сэмпл,
    # что бессмысленно увеличивало размер FFT без прироста разрешения.
    n_fft: int       = 2048
    hop_length: int  = 512     # ~23 мс — стандарт librosa для музыки
    win_length: int  = 1024    # ~46 мс — реальное окно анализа

    # ── Mel-фильтрбанк (ОБЯЗАТЕЛЬНО использовать) ───────────
    # Mel-шкала логарифмически распределяет частоты по восприятию слуха,
    # что критично для распознавания нот (каждая октава — вдвое по частоте).
    # Без Mel: линейная шкала даёт 128 бин из 1025 — низкочастотное
    # разрешение катастрофически плохое.
    use_mel: bool    = True
    n_mels: int      = 128
    fmax: float      = 8000.0  # верхняя граница Mel-фильтра (Гц)
                                # 8000 покрывает всю музыкальную высоту (до G9 ≈ 6272 Гц)

    # ── Логарифмическое масштабирование (обязательно) ────────
    log_scale: bool  = True    # power_to_db: диапазон ~[-80, 0] дБ
